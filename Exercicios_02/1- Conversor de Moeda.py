

# Conversão de reais para dólares e euros

valor_reais = 100.00
taxa_dolar = 5.20
taxa_euro = 6.15

valor_em_dolar = valor_reais / taxa_dolar
valor_em_euro = valor_reais / taxa_euro

print("Valor em reais: R$ {:.2f}".format(valor_reais))
print("Valor em dólares: US$ {:.2f}".format(valor_em_dolar))
print("Valor em euros: € {:.2f}".format(valor_em_euro))
